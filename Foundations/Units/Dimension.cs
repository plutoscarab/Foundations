namespace Foundations.Units
{
    /// <summary>
    /// Dimensions of measurement in a system of measurement.
    /// </summary>
    public enum Dimension
    {
        /// <summary>
        /// Length dimension.
        /// </summary>
        Length,

        /// <summary>
        /// Mass dimension.
        /// </summary>
        Mass,

        /// <summary>
        /// Time dimension.
        /// </summary>
        Time,

        /// <summary>
        /// Electric current dimension.
        /// </summary>
        Current,

        /// <summary>
        /// Thermodynamic temperature dimension.
        /// </summary>
        Temperature,

        /// <summary>
        /// Amount of substance dimension.
        /// </summary>
        Amount,

        /// <summary>
        /// Luminous intensity dimension.
        /// </summary>
        Intensity,
    }
}